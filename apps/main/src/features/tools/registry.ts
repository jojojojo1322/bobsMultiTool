export const tools: ToolDefinition[] = baseTools.map(withDemandDetails);

export const toolCategories = Array.from(new Set(tools.map((tool) => tool.category)));

export function getToolBySlug(slug: string) {
  return tools.find((toolItem) => toolItem.slug === slug);
}

export function getRelatedTools(slugs: string[]) {
  return slugs.map((slug) => getToolBySlug(slug)).filter((toolItem): toolItem is ToolDefinition => Boolean(toolItem));
}

export function getToolSearchText(tool: ToolDefinition) {
  return [
    tool.title,
    tool.shortTitle,
    tool.slug,
    tool.category,
    tool.description,
    tool.contentCluster,
    tool.demandTier,
    tool.monetizationTier,
    ...tool.searchIntents,
    ...tool.aliases,
    ...tool.useCases,
    ...tool.inputExamples,
    ...(tool.failureCases ?? []),
    ...(tool.preCopyChecklist ?? []),
    ...tool.seo.keywords,
    tool.seo.title,
    tool.seo.description,
    ...tool.examples.flatMap((example) => [example.label, example.value, example.note]),
    ...tool.faqs.flatMap((faq) => [faq.question, faq.answer]),
    ...tool.guides.map((guideLink) => guideLink.title),
  ].join(" ");
}

function normalizeSearch(value: string) {
  return value.toLowerCase().replace(/[^a-z0-9가-힣一-龥ぁ-んァ-ン]+/g, " ").trim();
}

export function scoreToolSearch(tool: ToolDefinition, query: string) {
  const normalizedQuery = normalizeSearch(query);
  if (!normalizedQuery) return demandWeight[tool.demandTier];

  const title = normalizeSearch(tool.title);
  const shortTitle = normalizeSearch(tool.shortTitle);
  const slug = normalizeSearch(tool.slug.replace(/-/g, " "));
  const aliases = tool.aliases.map(normalizeSearch);
  const keywords = tool.seo.keywords.map(normalizeSearch);
  const intents = tool.searchIntents.map(normalizeSearch);
  const searchText = normalizeSearch(getToolSearchText(tool));
  const terms = normalizedQuery.split(/\s+/).filter(Boolean);

  let score = demandWeight[tool.demandTier];
  if (title === normalizedQuery || shortTitle === normalizedQuery || slug === normalizedQuery) score += 120;
  if (title.startsWith(normalizedQuery) || slug.startsWith(normalizedQuery)) score += 70;
  if (aliases.some((alias) => alias === normalizedQuery)) score += 90;
  if (aliases.some((alias) => alias.includes(normalizedQuery))) score += 55;
  if (keywords.some((keyword) => keyword === normalizedQuery) || intents.some((intent) => intent === normalizedQuery)) score += 65;
  if (keywords.some((keyword) => keyword.includes(normalizedQuery)) || intents.some((intent) => intent.includes(normalizedQuery))) score += 35;

  for (const term of terms) {
    if (title.includes(term) || slug.includes(term)) score += 24;
    if (aliases.some((alias) => alias.includes(term))) score += 18;
    if (keywords.some((keyword) => keyword.includes(term)) || intents.some((intent) => intent.includes(term))) score += 14;
    if (searchText.includes(term)) score += 5;
  }

  return score;
}

export function searchTools(query: string, source: ToolDefinition[] = tools) {
  const normalizedQuery = normalizeSearch(query);
  if (!normalizedQuery) return source;
  return source
    .map((toolItem) => ({ tool: toolItem, score: scoreToolSearch(toolItem, normalizedQuery) }))
    .filter((item) => item.score > demandWeight[item.tool.demandTier])
    .sort((a, b) => b.score - a.score || a.tool.title.localeCompare(b.tool.title))
    .map((item) => item.tool);
}
